package com.tka.Oct29;

public class Arithmetic {
	public  static void main(String[] args) {
		int n = 45/2;
		System.out.println(n);
		
		byte b = 3;
		long d = 7;
		System.out.println(d/b);
		
		char c = 'A';
		char d1 = 'B';      
		System.out.println(c+d1); //ASCII value sum
		
		System.out.println(c+d);  //ASCII value sum
		
		
		//Modules 
		
		int f = 4/2;
		System.out.println(f);
	}
}
